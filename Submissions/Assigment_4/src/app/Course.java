package app;

public class Course {

  String name;
  String couseId;
  int numberOfUnits;
  double grade;
  int semestersCoursed;

  public Course(String name, String couseId, int numberOfUnits) {
    super();
    this.name = name;
    this.couseId = couseId;
    this.numberOfUnits = numberOfUnits;
    this.grade = grade;
    this.semestersCoursed = 0;
    this.grade = -1;
  }
}
